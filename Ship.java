package cs5800_Builder;

public class Ship {
	private String hull;
	private String deck;
	private String funnel;
	private String mast;
	private String forecastle;
	private String bridge;
	
	public Ship() {
		
	}
	
	public void setHull(String hull) {
		this.hull = hull;
	}
	
	public void setDeck(String deck) {
		this.deck = deck;
	}
	
	public void setFunnel(String funnel) {
		this.funnel = funnel;
	}
	
	public void setMast(String mast) {
		this.mast = mast;
	}
	
	public void setForecastle(String forecastle) {
		this.forecastle = forecastle;
	}
	
	public void setBridge(String bridge) {
		this.bridge = bridge;
	}
}
